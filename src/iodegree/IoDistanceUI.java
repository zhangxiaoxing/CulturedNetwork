    private void performQuery(String file) {
        degree = LegacyDB.degreeQuery(file, boolGlu, boolInput, div);
        totalConn = degree[1] + degree[2] * 2 + degree[3] * 3;
        txt1Deg.setText(Integer.toString(degree[1]));
        txt2Deg.setText(Integer.toString(degree[2]));
        txt3Deg.setText(Integer.toString(degree[3]));
        txtTotalConn.setText(Integer.toString(totalConn));
    }

    private void countSlot(String file) {
        QryResult result = LegacyDB.nSlotQuery(file, boolGlu, div);
        currTypeInGrp = result.getCurrTypeInGrp();
        groupSize = result.getGroupSize();
        nCurrTypeCell = result.getCurrTypeCell();
        nBothTypeCell = result.getBothTypeCell();

        if (boolInput) {
            txtTotalCell.setText(Integer.toString(nBothTypeCell));
            degree[0] = nBothTypeCell - degree[1] - degree[2] - degree[3];
            txt0Deg.setText(Integer.toString(degree[0]));
        } else {
            txtTotalCell.setText(Integer.toString(nCurrTypeCell));
            degree[0] = nCurrTypeCell - degree[1] - degree[2] - degree[3];
            txt0Deg.setText(Integer.toString(degree[0]));
        }
    }

