    private void btnCompareActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCompareActionPerformed
        arrRndLess = new int[4];
        arrRndGreater = new int[4];
        arrRndAvg = new int[4];


        for (int i = 0; i < genCycle; i++) {
            for (int j = 0; j < 4; j++) {
                arrRndAvg[j] += arrRndGen[i][j];
                if (arrRndGen[i][j] <= degree[j]) {
                    arrRndLess[j]++;
                }
                if (arrRndGen[i][j] >= degree[j]) {
                    arrRndGreater[j]++;
                }
            }
        }

        txtLess0.setText(Float.toString((float) arrRndLess[0] / genCycle));
        txtGreater0.setText(Float.toString((float) arrRndGreater[0] / genCycle));
        txtLess1.setText(Float.toString((float) arrRndLess[1] / genCycle));
        txtGreater1.setText(Float.toString((float) arrRndGreater[1] / genCycle));
        txtLess2.setText(Float.toString((float) arrRndLess[2] / genCycle));
        txtGreater2.setText(Float.toString((float) arrRndGreater[2] / genCycle));
        txtLess3.setText(Float.toString((float) arrRndLess[3] / genCycle));
        txtGreater3.setText(Float.toString((float) arrRndGreater[3] / genCycle));

        txtAvg0.setText(Float.toString((float) arrRndAvg[0] / genCycle));
        txtAvg1.setText(Float.toString((float) arrRndAvg[1] / genCycle));
        txtAvg2.setText(Float.toString((float) arrRndAvg[2] / genCycle));
        txtAvg3.setText(Float.toString((float) arrRndAvg[3] / genCycle));

    }//GEN-LAST:event_btnCompareActionPerformed

